// Representation of a date.
public class Date
{
	// The day, month and year of the date.
	public int day, month, year;

	// Construct a date -- given the required day, month and year.
	public Date(int RequiredDay, int RequiredMonth, int RequiredYear)
	{
		day = RequiredDay;
		month = RequiredMonth;
		year = RequiredYear;
	} // Date

} // class Date